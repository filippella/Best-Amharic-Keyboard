    public GradientDrawable getGradient(KeyThemeInfo themeInfo) {
        GradientDrawable drawable = new GradientDrawable(GradientDrawable.Orientation.TOP_BOTTOM, new int[]{themeInfo.getColorA(), themeInfo.getColorB(), themeInfo.getColorC()});
        drawable.setCornerRadius(0f);
        drawable.setStroke(1, themeInfo.getColorC());
        return drawable;
    }
